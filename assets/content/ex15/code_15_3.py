def f(x, a):
    # переписываем функцию из условия
    return (x & a != 0) <= ((x & 698 == 0) <= (x & 321 != 0))

# создаем массив для возможных ответов
ans = []
# перебираем ашки до примерно 10 000 (зависит от бóльшего числа в задаче)
for a in range(1, 10_000):
    # и смотрим чтобы для всех иксов выполнялась истинность ф-ии
    if all(f(x, a) for x in range(1, 10_000)): ans.append(a)
# находим максимальный из ответов
print(max(ans))