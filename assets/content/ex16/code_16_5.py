from functools import *

# записвыаем функцию из условия
@lru_cache(None)
def f(n):
    # если n < 20
    if n < 20: return n
    # n точно >= 20
    return (n - 6) * f(n - 7)

# функция зависит от предыдущих значений,
# поэтому перебираем значения по возрастанию
for n in range(47900): f(n)

# записываем вопрос из условия
print( (f(47872) - 290 * f(47865)) / f(47858) )