def F(n):
    # если число <= 10
    if n <= 10:
        return n
    # число точно больше 10
    # если число четно
    if n % 2 == 0:
        return 2 * F(n - 2) + 6
    # число точно нечетное и больше 10
    return F(n - 1) + 2 * n

# вернем сумму цифр ответа
print(sum(map(int, str(F(27) - F(20)))))