from re import *

s = open('24_15.txt').readline()

# Регулярное выражение будет искать строку из:
# — Одной или более буквы от a до z
# — Символа собачки
# — Одной или более буквы от a до z
# — Символа точки
# — Одной или более буквы от a до z
a = [x.group() for x in finditer(r'[a-z]+@[a-z]+.[a-z]+', s)]

# ищем среди подстрок максимальную по длине
res = max(a, key=len)
print(len(res))