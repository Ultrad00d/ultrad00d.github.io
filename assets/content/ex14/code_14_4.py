# возьмем из модуля string весь алфавит
from string import *

# весь алфавит из 36 символов
# (больше, чем нужно для этой задачи)
alph = '0123456789' + ascii_lowercase

# берем по цифре из первых 29 символов
for x in alph[:29]:
    # и записываем для них операнды в 10й сс
    a = int(f'463{x}7921', 29)
    b = int(f'8241{x}153', 29)
    # если сумма кратна 28, то
    if (a + b) % 28 == 0:
        # выписываем частное от деления на 28
        print((a + b) // 28)
        break