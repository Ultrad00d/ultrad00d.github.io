# очень большое число из условия
s = 17 * 125 ** 453 + 117 * 5 ** 231 - 3 * 5 ** 13 - 2357
# будем записывать цифры числа
res = []
while s > 0:
    # будем находить остаток от 125
    res.append(s % 125)
    # и уменьшать число в 125 раз
    s //= 125
# среди полученных цифр, найдем те, которые <=37 и найдем
# их количество путем нахождения длины полученного массива
print(len([x for x in res if x <= 37]))