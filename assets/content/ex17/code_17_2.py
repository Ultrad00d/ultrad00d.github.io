# например, даны числа:
s = [14, 28, 280, -28028, -1488]

# берем каждое число
for i in s:
    # проверяем, какой остаток дает число на 10 в
    # степени количества цифр числа, на которое должно
    # оканчиваться исходное число.
    # 28 = 2 цифры, 10 ** 2 = 100
    if abs(i) % 100 == 28: print( i )

# берем каждое число
for i in s:
    # смотрим, если строковое представление числа
    # со срезом до двух последних цифр является
    # искомым числом
    if str(i)[-2:] == '28': print( i )