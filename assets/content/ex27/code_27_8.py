from math import dist

def density(cl):
    # Создаем массив для сравнения
    m = []
    # Перебираем все точки кластера, чтобы найти 
    # кол-во точек в единичной окружности около
    # нее (по методу, схожему с DBSCAN)
    for p in cl:
        # Находим количество точек на расстоянии 
        # меньше 1 для выбранной точки
        k = len([p1 for p1 in cl if dist(p, p1) <= 1])
        m.append(k)
    # Находим плотность через среднее арифметическое (по усл.)
    return sum(m)/len(m)