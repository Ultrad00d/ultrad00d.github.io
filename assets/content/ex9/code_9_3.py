A = [1, 2, 3, 4, 5, 6]
B = [1, 2, 2, 4, 4, 6]

# Будем проверять, совпадает ли исходная длина 
# массива с длиной уникального набора (set)
# этого же массива:
if len(set(A)) == len(A):
    print('Числа в A не повторяются')
    # Числа в A не повторяются

if len(set(B)) == len(B):
    print('Числа в B не повторяются')